class User:
    def __init__(self, index, name, userid, password, usertype, avatar, bio, country, occupation, DOB, verification) -> None:
        self.index = index
        self.name = name
        self.userid = userid
        self.password = password
        self.usertype = usertype
        self.avatar = avatar
        self.bio = bio
        self.country = country
        self.occupation = occupation
        self.DOB = DOB
        self.verification = verification
